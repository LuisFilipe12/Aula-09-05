
public class Produto {

	public int id;
	public char operador;

	public double Calcular(double n1, double n2) {
		return n1 + n2;

	}

	public double Calcular(double n1, double n2, double n3) {
		return n1 + n2 + n3;
	}

	public double Calcular(double n1, double n2, double n3, double n4) {
		return n1 + n2 + n3 + n4;
	}
}
